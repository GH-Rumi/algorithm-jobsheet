package day7.codeprogram;

public class Dosen11 {
    String nama;
    int usia;

    public Dosen11(String nama, int usia) {
        this.nama = nama;
        this.usia = usia;
    }

    public String Nama() {
        return nama;
    }

    public int Usia() {
        return usia;
    }

    public String toString() {
        return "Nama: " + nama + ", Usia: " + usia;
    }
}
